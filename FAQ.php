            <h1>faq</h1>
            <h2>En lien avec les cours:</h2>
            <p>
                <strong>Quels sont les âges pour faire du BMX?</strong></br>
                On peut faire du BMX de 3 à 99 ans.
            </p>
            <p>
                <strong>Quel est l’horaire des cours?</strong></br>
                Voir la section Cours et inscription. L’information y est placée selon l’âge de l’enfant.
            </p>
            <p>
                <strong>À combien de participants par cours peut-on s’attendre ?</strong></br>
                Nous essayons d’avoir des groupes de 8 à 12 participants, selon l’âge. 
                En fonction du nombre d’inscriptions, nous offrons des heures de cours supplémentaires.
            </p>
            <p>
                <strong>Mon enfant n’a jamais fait de BMX. Est-ce que les cours seront trop difficiles pour lui ou elle?</strong></br>
                Les groupes sont formés en fonction de l’âge des enfants. 
                Les exercices sont ajustés en fonction de l’habileté des participants.​
            </p>
            <p>
                <strong>Est-ce que les cours ont lieu en cas de mauvais temps?</strong></br>
                En cas de pluie ou d’orage, il se peut que le cours soit annulé.</br>
                Nous afficherons l’information sur la page Facebook du club au moins une heure avant le début du cours si le cours est annulé.
            </p>
            <p>
                <strong>Y a-t-il risque de blessures?</strong></br>
                Comme c’est un sport extrême, il y a risque de chutes. C’est pourquoi le port du casque intégral est obligatoire. 
                Le pantalon et chandail longs ainsi que les gants à doigts fermés sont aussi obligatoires. Le Club suggère aussi fortement les protecteurs à genoux, coudes et plastron 
                afin d’éviter le plus possible les blessures en cas de chute.
            </p>
        </section>
        <section class="texteFaq">
            <h2>En lien avec la piste:</h2>
            <p>
                <strong>La piste est-elle réservée pour les cours?</strong></br>
                Durant les activités de BMX Montréal, la piste est réservée à l’usage exclusif du club Pour des raisons de sécurité, 
                aucune autre personne n’est admise sur la piste (Les cours sont du lundi au vendredi de 17h30 à 21h30.)
            </p>
            <p>
                <strong>​La piste est-elle éclairée?</strong></br>
                Oui, la piste maintenant éclairée. Elle est ouverte de 7h à 23h. 
            </p>
        </section>
        <section class="texteFaq">
            <h2>En lien avec l'équipement requis:</h2>
            <p>
                <strong>Puis-je suivre le cours avec un vélo de montagne?</strong></br>
                Le vélo de type BMX n’est pas obligatoire. Cependant, il sera difficile de faire certains exercices avec un vélo de montagne.
            </p>
            <p>
                <strong>Quels sont les types de BMX?</strong></br>
                Il y a principalement deux types de BMX: les free Style (pour faire des acrobaties) 
                et ceux de course (sur piste). BMX Montréal est un club de course sur piste.
            </p>
            <p>
                <strong>Y a-t-il différentes tailles de BMX?</strong></br>
                Dans le BMX de course, les tailles sont les suivantes: 
                Micro, Mini, Junior, Expert, Pro et ProXL.
            </p>
            <p>
                <strong>Quelle préparation dois-je faire au vélo de mon enfant?</strong></br>
                Un vélo en bon état et bien ajusté est requis. Les réflecteurs et la béquille doivent être enlevés. 
                Le participant devra être capable de freiner efficacement et de s’arrêter rapidement. 
                Le frein à rétropédalage n’est pas recommandé.
            </p>
            <p>
                <strong>Quel est l’équipement requis dans le cadre des cours?</strong></br>
                Un chandail à manches longues, un pantalon long, des gants avec doigts fermés ainsi qu’un casque intégral sont obligatoires.
                Voir la section Règlements pour plus de détails.
            </p>
            <p>
                <strong>Quand puis-je commander un maillot du club?</strong></br>
                Votre enfant pourra essayer les différentes grandeurs de maillots lors de la séance d’information générale. 
                Vous pourrez le commander sur place ou via notre site internet. Les différentes tailles sont les suivantes: 
                enfants (6, 8, 10, 12, 14, 16) et adultes (XS, S, M, L, XL, XXL).
            </p>
            <p>
                <strong>Le maillot du club est-il obligatoire?</strong></br>
                Le maillot du club n’est pas obligatoire lors des cours. Par contre, si votre enfant veut participer aux compétitions, 
                le maillot est obligatoire.
            </p>
        </section>

